package project.volunion.model;

public class VolunionInfo {

    public String name;
    public String surname;
    public String job;
    public String email;
    public String city;
    public String town;
    public String company;

    public VolunionInfo(String name, String surname, String job, String email, String city, String town, String company) {
        this.name = name;
        this.surname = surname;
        this.job = job;
        this.email = email;
        this.city = city;
        this.town = town;
        this.company = company;
    }
}
